export function getCalendarLocale(language: string): string {
  if (language.startsWith('pt')) return 'pt';
  if (language.startsWith('en')) return 'en';
  return 'en';
}

export function configureCalendarLocale(locale: string): string {
  LocaleConfig.defaultLocale = locale;
  return locale;
}
